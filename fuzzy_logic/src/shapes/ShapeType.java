package shapes;

/**
 * LATRI: left angled triangular<br>
 * RTRI: Right angled triangular<br>
 * NTRI: normal triangular<br>
 * LTRAP: left angled trapezoid<br>
 * RTRAP: Right angled trapezoid<br>
 * NTRAP:normal trapezoid<br>
 * EPS_QUAD: equal parallel sides quadrilateral (ex: squares or rectangles)
 */
public enum ShapeType {
	LATRI, RATRI, NTRI, LATRAP, RATRAP, NTRAP, EPS_QUAD
}
